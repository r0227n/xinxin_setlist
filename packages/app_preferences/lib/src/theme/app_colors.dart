import 'package:flutter/material.dart';

/// XINXIN color constants
///
/// This class defines the core color palette for the Xinxin setlist
/// application. All colors are defined as static constants to ensure
/// consistency across the entire application.
class XINXINColors {
  /// Creates an instance of [XINXINColors]
  ///
  /// This constructor is private to prevent instantiation since this class
  /// only contains static constants.
  const XINXINColors._();

  /// Primary orange color for the Xinxin brand
  ///
  /// HEX: #F39801
  /// RGB: (243, 152, 1)
  static const orange = Color(0xFFF39801);

  /// Primary white color for text and contrast
  ///
  /// HEX: #FFFFFF
  /// RGB: (255, 255, 255)
  static const white = Color(0xFFFFFFFF);

  /// Primary black color
  ///
  /// HEX: #000000
  /// RGB: (0, 0, 0)
  static const black = Color(0xFF000000);
}
